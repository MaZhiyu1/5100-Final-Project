    public void refreshTable1(){
        DefaultTableModel model = (DefaultTableModel)Patienttbl.getModel();
        model.setRowCount(0);

        if(bz.getPatients()==null) return;
        for(Patient s : bz.getPatients()) {
            Object row[] = new Object[4];
            row[0] = s;
            row[1] = s.getName();
            row[2] = s.getPwd();
            row[3] = s.getEnabled();
            model.addRow(row);
        }
    }
    
    
    
